from cx16_conv.array import bytes_to_c_array


EXPECTED_C_ARRAY = """
uint8_t SYMBOL[22] = {
    0x4a, 0x75, 0x73, 0x74, 0x20, 0x61, 0x20, 0x73, 0x6d, 0x61, 0x6c, 0x6c, 0x20, 0x62, 0x79, 0x74,
    0x65, 0x61, 0x72, 0x72, 0x61, 0x79
};
""".strip()


def test_bytes_to_c_array():
    result = bytes_to_c_array("SYMBOL", b"Just a small bytearray")
    assert result == EXPECTED_C_ARRAY
